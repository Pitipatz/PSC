export interface ComplaintFormData {
  productTypeId: string; // รับเป็น string จาก Select แล้วค่อยแปลงเป็น number
  mainIssueId: string;
  subIssueId: string;
  channelId: string;
  subject: string;
  description: string;
  otherIssueDetail?: string;
  // ข้อมูลผู้แจ้ง (Optional ถ้าดึงจาก Context)
  name: string;
  email: string;
  phone?: string;
}

