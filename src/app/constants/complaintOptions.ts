// 1. ประเภทพื้นฐานสำหรับตัวเลือก (Options)
export interface MasterOption {
  id: number | string;
  label: string;
}

export interface ProductOption extends MasterOption {
  id: number;
  isOwnProduct: boolean; // สำหรับแยกสินค้าของบริษัท
}

// 2. โครงสร้างข้อมูลหลักของเรื่องร้องเรียน (ตามมาตรฐาน ธปท.)
export interface Complaint {
  id?: string;
  ticketNo: string;        // เลขที่รับเรื่อง (เช่น COMP-202604-001)
  userId: string;          // ผู้แจ้ง
  
  // --- ข้อมูลผลิตภัณฑ์และปัญหา ---
  productTypeId: number;   // 1-21
  mainIssueId: number;     // 1-15
  subIssueId: string;      // "1.1", "13.5"
  otherIssueDetail?: string; // ระบุเพิ่มเติมกรณีเลือก 15.1
  channelId: number;       // 1-5 (ช่องทางที่เกิดปัญหา)
  
  // --- รายละเอียด ---
  subject: string;
  description: string;
  
  // --- สถานะและ Flow การจัดการ ---
  status: 'pending' | 'processing' | 'resolved' | 'closed';
  currentHandlerId?: string; // ผู้รับผิดชอบปัจจุบัน
  
  // --- ข้อมูลสำหรับรายงาน ธปท. (Admin เป็นคนกรอกตอนปิดเคส) ---
  isProviderFault?: boolean;   // สาเหตุเกิดจากผู้ให้บริการหรือไม่
  receivedQuarter: string;      // งวดที่รับเรื่อง (เช่น "2026-Q2")
  resolvedQuarter?: string;     // งวดที่ยุติเรื่อง
  slaCategory?: string;        // "1-7 วัน", "8-15 วัน" ฯลฯ
  
  createdAt: string;           // ISO Date
  resolvedAt?: string;         // ISO Date

  reportMonth: string;         // เก็บเป็น "YYYY-MM" เช่น "2026-04" เพื่อใช้ Group ยอดรายเดือน
  
  // งวดที่รับเรื่อง (ธปท. นับวันที่รับเป็นสำคัญ)
  receivedDate: string;        // ISO Date (YYYY-MM-DD)
  // งวดที่ยุติเรื่อง
  resolvedDate?: string;       // ISO Date (YYYY-MM-DD)
  
  // ระยะเวลาจัดการ (ใช้คำนวณ SLA รายเดือน)
  processingDays?: number;     // จำนวนวันที่ใช้จัดการ (นับวันปฏิทิน)
}

